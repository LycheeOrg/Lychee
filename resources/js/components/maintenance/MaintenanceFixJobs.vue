<template>
	<Card v-if="data !== undefined && data > 0" class="min-h-40 shadow shadow-surface-950/30 rounded-lg relative">
		<template #title>
			<div class="text-center">
				{{ $t("maintenance.fix-jobs.title") }}
			</div>
		</template>
		<template #content>
			<ScrollPanel class="w-full h-40 text-sm text-muted-color">
				<div v-html="description" class="w-full text-center"></div>
				<ProgressSpinner v-if="loading" class="w-full"></ProgressSpinner>
			</ScrollPanel>
			<div class="flex gap-4 mt-1">
				<Button v-if="data > 0 && !loading" severity="primary" class="w-full" @click="exec">{{ $t("maintenance.fix-jobs.button") }}</Button>
			</div>
		</template>
	</Card>
</template>

<script setup lang="ts">
import ProgressSpinner from "primevue/progressspinner";
import MaintenanceService from "@/services/maintenance-service";
import Button from "primevue/button";
import Card from "primevue/card";
import { computed, ref } from "vue";
import ScrollPanel from "primevue/scrollpanel";
import { sprintf } from "sprintf-js";
import { trans } from "laravel-vue-i18n";
import { useToast } from "primevue/usetoast";

const data = ref(undefined as number | undefined);
const loading = ref(false);
const toast = useToast();

const description = computed(() => {
	return sprintf(trans("maintenance.fix-jobs.description"), "ready", "started", "failed");
});

function load() {
	MaintenanceService.jobsGet().then((response) => {
		data.value = response.data;
	});
}

function exec() {
	loading.value = true;
	MaintenanceService.jobsDo().then((response) => {
		toast.add({ severity: "success", summary: "Success" });
		loading.value = false;
	});
}

load();
</script>

<style lang="css" scoped>
.lychee-dark .p-card {
	--p-card-background: var(--p-surface-800);
}
</style>
